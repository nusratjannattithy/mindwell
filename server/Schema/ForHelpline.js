const mongoose = require('mongoose');

const helplineSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const HelplineMessage = mongoose.model('HelplineMessage', helplineSchema);

module.exports = HelplineMessage;
